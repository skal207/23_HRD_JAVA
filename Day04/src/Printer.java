
public class Printer {
	
	
	
	public static void println(int n ) {
		System.out.println(n);
	}
	public void println(boolean n ) {
		System.out.println(n);
	}
	public void println(double n ) {
		System.out.println(n);
	}
	public void println(String n ) {
		System.out.println(n);
	}

}
